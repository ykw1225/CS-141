public class Egg2
{
	public static void main(String[]args)
	{
		head();
		tail();
		mid();
		head();
		tail();
		mid();
		tail();
		head();
		mid();
		tail();
	}
	public static void head()
	{
		System.out.println("  _______");
		System.out.println(" /       \\");
		System.out.println("/         \\");
	}
	public static void mid()
	{
		System.out.println("-\"-'-\"-'-\"-");
	}
	public static void tail()
	{
		System.out.println("\\         /");
		System.out.println(" \\_______/");
	}
}